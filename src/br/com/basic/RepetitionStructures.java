package src.br.com.basic;

public class RepetitionStructures {
    public static void main(String[] args) {
        // While
        int initialValue = 0;
        while (initialValue < 4) {
            System.out.println("The initial value is less than 4");
            initialValue++;
        }

        // For
        for (int i = 0; i < 4; i++) {
            System.out.println("For structure -> The i values is: " + i);
        }
    }
}
